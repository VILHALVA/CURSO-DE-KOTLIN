package org.kotlinlang.play        

fun main() {                        
  var numero = 3.567765443445 // var numero: Double = 3.567765443445
  var numero1 = "VILHALVA" // var numero: String = "VILHALVA"
  var numero2: Float = 8.9f
  var numero3 = 8.9
  var numero4 = 10
  var numero5: Long = 23456666L
  var resultado = true // VARIAVEL BOOLEANA.
  var resultado1: Boolean = false
  println(numero)
  println(numero1)
  println(numero2)
  println(numero3)
  println(numero4)
  println(numero5)
  println(resultado)
  println(resultado1)
}

/* DIFERENTE DO JAVA, NO KOTLIN TEMOS INFERENCIA AUTOMATICA: 
 * NÃO É PRECISO INFERIR O TIPO DA VARIAVEL; COMO:
 * STRING, FLOAT, INT, DOUBLE, LONG...
 * CASO QUEIRA INFERIR: É PRECISO COLOCAR A PRIMEIRA LETRA DO TIPO NO FIM DO ATRIBUTO.
 */